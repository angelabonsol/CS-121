
public class AssignmentOps {
	   public static void main(String[] args)
	   {
	      int x = 0, y = 0;
	  
	      x = 7;
	      y = 5;
	      
	      x = x * y - 3;
	  
	      System.out.println("Value of x: " + x);
	  
	      x = 7;
	      y = 5;
	      
	      x *= y - 3;
	  
	      System.out.println("Value of x: " + x);
	   }
	}
